int rowSumOddNumbers(int n) {
  return n * n * n; 
}

// Tiene 5 filas y cada número de esas filas son números impares.
// La suma de cada fila es n al cubo SIEMPRE.

void main() {
  print(rowSumOddNumbers(1)); 
  print(rowSumOddNumbers(2)); 
  print(rowSumOddNumbers(3)); 
  print(rowSumOddNumbers(4)); 
  print(rowSumOddNumbers(5));
}